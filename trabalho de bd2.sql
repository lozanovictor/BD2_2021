CREATE OR REPLACE FUNCION limpa_salario
RETURNS INT
AS 
$$
  UPDATE Funcionario SET f_conbank = 0 WHERE f_Situacao = FALSE;
$$
LANGUAGE 'plpgsql';

CREATE TRIGGER limpar_salario
AFTER UPDATE ON Funcionario FOR EACH ROW
EXECUTE PROCEDURE limpa_salario();


